"""Read-only catalog of pre-organized platform datasets.

A teacher curates datasets into a normalized directory tree (one folder per
dataset, each holding train/<label>/, test/<label>/ and a platform_dataset.json
manifest). This module scans that tree and exposes the datasets that match the
platform's trainable capabilities, so step 1 can offer a "import an organized
dataset" dropdown instead of asking students to upload files by hand.

Nothing here writes; importing the actual samples lives in ProjectService.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

# Only datasets under this subfolder feed the four-step workflow; everything in
# 02_暂不纳入平台主流程/ (detection, model weights, raw text) is intentionally skipped.
DIRECT_USE_SUBDIR = "01_可直接用于平台主流程"

# capabilities the in-app trainer supports — a manifest tagged with anything else
# is ignored so the dropdown never offers something the platform cannot train.
SUPPORTED_CAPABILITIES = {
    "image_classifier",
    "text_classifier",
    "audio_classifier",
    "sensor_decision_model",
}

MANIFEST_FILE = "platform_dataset.json"

# A class label is a folder name, never a file. Some manifests were generated by
# globbing a directory that also held loose images, so they list phantom entries
# like "0.jpg" beside the real "0". Drop anything that looks like a filename.
_MEDIA_SUFFIXES = (".jpg", ".jpeg", ".png", ".bmp", ".gif", ".webp", ".wav")


def _clean_labels(labels: Any) -> list[str]:
    if not isinstance(labels, list):
        return []
    return [
        str(label)
        for label in labels
        if isinstance(label, str) and not label.lower().endswith(_MEDIA_SUFFIXES)
    ]


def _read_manifest(manifest_path: Path) -> dict[str, Any] | None:
    try:
        data = json.loads(manifest_path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return None
    if not isinstance(data, dict):
        return None
    capability = data.get("ai_capability")
    dataset_id = data.get("id")
    if capability not in SUPPORTED_CAPABILITIES or not dataset_id:
        return None
    return data


def _entry(manifest: dict[str, Any], dataset_dir: Path) -> dict[str, Any]:
    """Shape one catalog row for the API/UI (path stays server-side only)."""
    labels = _clean_labels(manifest.get("labels"))
    return {
        "id": str(manifest["id"]),
        "title": manifest.get("title", dataset_dir.name),
        "ai_capability": manifest["ai_capability"],
        "labels": labels,
        "class_count": len(labels),
        "train_count": manifest.get("train_count", 0),
        "test_count": manifest.get("test_count", 0),
        "recommended_model": manifest.get("recommended_model", ""),
    }


def _scan(datasets_root: Path) -> list[tuple[dict[str, Any], Path]]:
    direct_dir = datasets_root / DIRECT_USE_SUBDIR
    if not direct_dir.is_dir():
        return []
    found: list[tuple[dict[str, Any], Path]] = []
    # Layout: 01_可直接用于平台主流程/<category>/<dataset>/platform_dataset.json
    for manifest_path in sorted(direct_dir.glob(f"*/*/{MANIFEST_FILE}")):
        manifest = _read_manifest(manifest_path)
        if manifest is not None:
            found.append((manifest, manifest_path.parent))
    return found


def list_datasets(datasets_root: Path, capability: str | None = None) -> list[dict[str, Any]]:
    """Catalog rows, optionally filtered to one capability. Empty if root absent."""
    rows = [_entry(manifest, dataset_dir) for manifest, dataset_dir in _scan(datasets_root)]
    if capability:
        rows = [row for row in rows if row["ai_capability"] == capability]
    return rows


def resolve_dataset(datasets_root: Path, dataset_id: str) -> tuple[dict[str, Any], Path] | None:
    """Find a dataset's manifest + directory by id.

    Matching against scanned ids (never user-supplied paths) keeps a request from
    pointing the importer at an arbitrary directory.
    """
    for manifest, dataset_dir in _scan(datasets_root):
        if str(manifest["id"]) == dataset_id:
            return manifest, dataset_dir
    return None
